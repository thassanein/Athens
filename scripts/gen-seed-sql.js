'use strict';

/**
 * Generates db/seed.sql — the starter OKR data (drivers, goals, key results,
 * projects) as plain SQL you can paste into the Neon SQL editor after running
 * db/schema.sql. This is the "no laptop required" path from DEPLOY.md.
 *
 * It intentionally does NOT include any user/admin row: admin credentials
 * (even hashed) should not be committed. Create the admin separately — either
 * via `npm run seed` (uses SEED_ADMIN_*) or a one-off INSERT.
 *
 * Run with:  node scripts/gen-seed-sql.js
 */

const fs = require('fs');
const path = require('path');
const { DRIVERS, GOALS, KEY_RESULTS, PROJECTS } = require('./seed-data');

const q = (v) => {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
};

const lines = [];
lines.push('-- Athens Command Center — starter data');
lines.push('-- Generated by scripts/gen-seed-sql.js. Run db/schema.sql first, then this.');
lines.push('-- Safe to re-run: rows use explicit ids with ON CONFLICT DO NOTHING.');
lines.push('-- Does NOT create a login — create your admin separately (see DEPLOY.md).');
lines.push('');
lines.push('BEGIN;');
lines.push('');

lines.push('INSERT INTO drivers (id, name, title, email) VALUES');
lines.push(
  DRIVERS.map((d, i) => `  (${i + 1}, ${q(d.name)}, ${q(d.title)}, ${q(d.email)})`).join(',\n') +
  '\nON CONFLICT (id) DO NOTHING;'
);
lines.push('');

lines.push('INSERT INTO goals (id, title, description, driver_id, status, progress, sort_order) VALUES');
lines.push(
  GOALS.map((g, i) =>
    `  (${i + 1}, ${q(g.title)}, ${q(g.description)}, ${g.driver + 1}, ${q(g.status)}, ${g.progress}, ${i})`
  ).join(',\n') + '\nON CONFLICT (id) DO NOTHING;'
);
lines.push('');

lines.push('INSERT INTO key_results (id, goal_id, title, driver_id, unit, start_value, current_value, target_value, status, progress, sort_order) VALUES');
lines.push(
  KEY_RESULTS.map((k, i) =>
    `  (${i + 1}, ${k.goal + 1}, ${q(k.title)}, ${GOALS[k.goal].driver + 1}, ${q(k.unit)}, ${k.start}, ${k.current}, ${k.target}, ${q(k.status)}, ${k.progress}, ${i})`
  ).join(',\n') + '\nON CONFLICT (id) DO NOTHING;'
);
lines.push('');

lines.push('INSERT INTO projects (id, key_result_id, title, description, driver_id, status, progress, start_date, due_date, sort_order) VALUES');
lines.push(
  PROJECTS.map((p, i) => {
    const goalIdx = KEY_RESULTS[p.kr].goal;
    return `  (${i + 1}, ${p.kr + 1}, ${q(p.title)}, ${q(p.description)}, ${GOALS[goalIdx].driver + 1}, ${q(p.status)}, ${p.progress}, ${q(p.start)}, ${q(p.due)}, ${i})`;
  }).join(',\n') + '\nON CONFLICT (id) DO NOTHING;'
);
lines.push('');

// Keep the SERIAL sequences ahead of the explicit ids we inserted.
lines.push("SELECT setval('drivers_id_seq', (SELECT max(id) FROM drivers));");
lines.push("SELECT setval('goals_id_seq', (SELECT max(id) FROM goals));");
lines.push("SELECT setval('key_results_id_seq', (SELECT max(id) FROM key_results));");
lines.push("SELECT setval('projects_id_seq', (SELECT max(id) FROM projects));");
lines.push('');
lines.push('COMMIT;');
lines.push('');

const outPath = path.join(__dirname, '..', 'db', 'seed.sql');
fs.writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${outPath} (${DRIVERS.length} drivers, ${KEY_RESULTS.length} KRs, ${PROJECTS.length} projects, ${GOALS.length} goals)`);
